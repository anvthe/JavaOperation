package BeginnerJava;

public class AssignmentDemo {
    public static void main(String[] args) {

        int x = 10;
        int y = 2;

        x += y;
        System.out.println("X = "+x);

        x -= y;
        System.out.println("X = "+x);

        x *= y;
        System.out.println("X = "+x);

        x /= y;
        System.out.println("X = "+x);

        x %= y;
        System.out.println("X = "+x);


    }

}
